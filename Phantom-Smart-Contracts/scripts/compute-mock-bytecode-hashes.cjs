#!/usr/bin/env node
/**
 * Recompute mock bytecode keccak256 fingerprints after `HH_FULL=1 npx hardhat compile`.
 * Writes core/config/module7-mock-bytecode-hashes.json (run from repo core root).
 */
const fs = require("fs");
const path = require("path");
const { ethers } = require("ethers");

const root = path.join(__dirname, "..", "..");
const artifactsDir = path.join(__dirname, "..", "artifacts", "contracts", "_full", "mocks");
const outPath = path.join(root, "config", "module7-mock-bytecode-hashes.json");

function loadArtifact(name) {
  const p = path.join(artifactsDir, `${name}.sol`, `${name}.json`);
  if (!fs.existsSync(p)) {
    console.error("Missing artifact:", p, "\nRun: cd Phantom-Smart-Contracts && HH_FULL=1 npx hardhat compile");
    process.exit(1);
  }
  return JSON.parse(fs.readFileSync(p, "utf8"));
}

const mv = loadArtifact("MockVerifier");
const ms = loadArtifact("MockSwapAdaptor");
const payload = {
  _comment: "Keccak256 of deployedBytecode from HH_FULL=1 compile (MockVerifier, MockSwapAdaptor). Re-run: node Phantom-Smart-Contracts/scripts/compute-mock-bytecode-hashes.cjs",
  compilerNote: "Regenerated by compute-mock-bytecode-hashes.cjs",
  mockVerifierBytecodeHash: ethers.keccak256(mv.deployedBytecode),
  mockSwapAdaptorBytecodeHash: ethers.keccak256(ms.deployedBytecode),
};
fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, JSON.stringify(payload, null, 2) + "\n");
console.log("Wrote", outPath);
console.log(payload);
